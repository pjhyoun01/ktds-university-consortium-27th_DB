-- 1. 100번 사원의 모든 부하직원을 계층조회한다. 107
 SELECT LEVEL
 	  , E.EMPLOYEE_ID 
   FROM EMPLOYEES E
  START WITH E.EMPLOYEE_ID = 100
CONNECT BY PRIOR E.EMPLOYEE_ID = E.MANAGER_ID
  ORDER BY LEVEL
;
-- 2. 113번 사원의 모든 상사를 계층조회한다. 4
 SELECT LEVEL
 	  , E.EMPLOYEE_ID 
   FROM EMPLOYEES E
  START WITH E.EMPLOYEE_ID = 113
CONNECT BY PRIOR E.MANAGER_ID = E.EMPLOYEE_ID
  ORDER BY LEVEL
;
-- 3. IT 부서장의 모든 부하직원을 계층조회한다. 5
 SELECT LEVEL
 	  , E.EMPLOYEE_ID 
   FROM EMPLOYEES E
  START WITH E.EMPLOYEE_ID = (SELECT MANAGER_ID
							   FROM DEPARTMENTS
							  WHERE DEPARTMENT_NAME = 'IT')		
CONNECT BY PRIOR E.EMPLOYEE_ID = E.MANAGER_ID
;
-- 4. 부서장들의 부하직원을 계층조회한다. 147
