	 , SYNOPSIS
	 , ORIGNAL_TITLE
	 , STATE
	 , LANGUAGE
	 , BUDGET
	 , PROFIT
  FROM MOVIES
 WHERE MOVIE_ID = 'MV-20260227-000001'
